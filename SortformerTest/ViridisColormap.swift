    /// Get color for a value between 0 and 1
    static func color(for value: Double) -> Color {
        let clampedValue = max(0, min(1, value))
        let index = Int(clampedValue * Double(colors.count - 1))
        let (r, g, b) = colors[index]
        return Color(red: r, green: g, blue: b)
    }
    
    /// Get CGColor for a value between 0 and 1 (for Canvas rendering)
    static func cgColor(for value: Float) -> CGColor {
        let clampedValue = max(0, min(1, value))
        let index = Int(clampedValue * Float(colors.count - 1))
        let (r, g, b) = colors[index]
        return CGColor(red: r, green: g, blue: b, alpha: 1.0)
    }
    
    /// Get RGB as UInt8 values for bitmap rendering
    static func rgb(for value: Float) -> (r: UInt8, g: UInt8, b: UInt8) {
        let clampedValue = max(0, min(1, value))
        let index = Int(clampedValue * Float(colors.count - 1))
        let (r, g, b) = colors[index]
        return (UInt8(r * 255), UInt8(g * 255), UInt8(b * 255))
    }
